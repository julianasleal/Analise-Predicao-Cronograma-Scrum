def engenharia_features(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    df["duracao_dias"]    = (df["dt_termino"] - df["dt_inicio"]).dt.days.clip(lower=0)
    df["var_pts_pct"]     = ((df["pts_real"] - df["pts_prev"]) / df["pts_prev"].replace(0, np.nan)) * 100
    df["var_sprint_pct"]  = ((df["sprint_exec"] - df["sprint_prev"]) / df["sprint_prev"].replace(0, np.nan)) * 100
    df["idx_eficiencia"]  = df["pts_real"] / df["pts_prev"].replace(0, np.nan)
    df["vel_prevista"]    = df["pts_prev"] / df["sprint_prev"].replace(0, np.nan)
    df["vel_realizada"]   = df["pts_real"] / df["sprint_exec"].replace(0, np.nan)

    for chave, (prev, exec_) in ETAPAS.items():
        col = chave.lower().replace(" ", "_")
        df[f"var_{col}_abs"] = df[exec_] - df[prev]
        df[f"var_{col}_pct"] = (df[f"var_{col}_abs"] / df[prev].replace(0, np.nan)) * 100
        df[f"prop_{col}"]    = df[exec_] / df["pts_real"].replace(0, np.nan)

    return df

def preparar_features(df: pd.DataFrame):
    """
    Modelo 1 - Predicao de DURACAO (retrospectivo).
    Features: dados realmente executados em projetos anteriores.
    Targets : sprint_exec, duracao_dias.
    """
    df = df.copy()
    encoders = {}
    for col in ("benfeitoria", "tipo_om", "tipo_obra"):
        le = LabelEncoder()
        df[f"{col}_enc"] = le.fit_transform(df[col].fillna("desconhecido"))
        encoders[col] = le

    # Features: tudo executado (sem sprint_exec, que e target)
    # arq_reaprov incluído diretamente — projetos com arquitetura
    # reaproveitada têm duração sistematicamente menor.
    feature_cols = [
        "area",
        "pts_real",
        "ep_exec",
        "ap_exec",
        "pe_arq_exec",
        "pe_eng_exec",
        "orc_exec",
        "benfeitoria_enc",
        "tipo_om_enc",
        "tipo_obra_enc",
        COL_ARQ_REAPROV,
    ]
    feature_cols = [c for c in feature_cols if c in df.columns]
    encoders["feature_cols"] = feature_cols

    X         = df[feature_cols].fillna(0)
    y_sprints = df["sprint_exec"].fillna(0)
    y_dias    = df["duracao_dias"]
    return X, y_sprints, y_dias, encoders

def preparar_features_planejamento(df: pd.DataFrame, encoders: dict):
    """
    Modelo 2 - Estimativa de PLANEJAMENTO (multi-output).

    Features base : area + tipo_obra  (melhor combinação LOO-CV)
    Feature condicional: arq_reaprov — incluída no LOO apenas quando
      n(flag=1) >= THRESHOLD_ARQ_REAPROV; sempre usada na predição de novos projetos.

    Targets: pts_prev total + pontos previstos por etapa.
    """
    df = df.copy()

    # Recodificar com encoders do Modelo 1 (garantia de consistência)
    for col in ("benfeitoria", "tipo_om", "tipo_obra"):
        enc = encoders.get(col)
        if enc:
            df[f"{col}_enc"] = enc.transform(df[col].fillna("desconhecido"))

    n_reaprov = int(df[COL_ARQ_REAPROV].sum()) if COL_ARQ_REAPROV in df.columns else 0
    usar_flag_loo = n_reaprov >= THRESHOLD_ARQ_REAPROV

    # Features para LOO-CV (treino/avaliação)
    feat_plan_loo = ["area", "tipo_obra_enc"]
    if usar_flag_loo:
        feat_plan_loo.append(COL_ARQ_REAPROV)

    # Features para predição de novos projetos (sempre inclui a flag)
    feat_plan_pred = ["area", "tipo_obra_enc", COL_ARQ_REAPROV]
    if "n_disc" in df.columns:
        feat_plan_pred.append("n_disc")
    feat_plan_pred = [c for c in feat_plan_pred if c in df.columns]

    if not usar_flag_loo and n_reaprov > 0:
        print(f"   ℹ️  arq_reaprov: {n_reaprov}/{THRESHOLD_ARQ_REAPROV} projetos — "
              f"excluída do LOO, ativa na predição de novos projetos.")

    alvos_plan = ["pts_prev", "ep_prev", "ap_prev",
                  "pe_arq_prev", "pe_eng_prev", "orc_prev"]
    alvos_plan = [c for c in alvos_plan if c in df.columns]

    X_plan_loo  = df[feat_plan_loo].fillna(0)
    X_plan_pred = df[feat_plan_pred].fillna(0)
    Y_plan      = df[alvos_plan].fillna(0)

    return X_plan_loo, X_plan_pred, Y_plan, feat_plan_loo, feat_plan_pred, alvos_plan

def _loo_metricas(modelo, X, y):
    """
    Calcula MAE, RMSE e R² (LOO-Q²) com LOO-CV acumulado.

    cross_val_score com LOO tem n=1 por fold: RMSE=MAE e R²=NaN por fold.
    Solução correta: acumular todos os resíduos e calcular globalmente.
      R² = 1 - SS_res / SS_tot  (Q² ou LOO-R²; pode ser negativo)
    """
    from sklearn.base import clone
    y_arr  = np.array(y, dtype=float)
    X_val  = X.values if hasattr(X, "values") else np.array(X)
    y_pred = np.zeros(len(y_arr))
    for tr, te in LeaveOneOut().split(X_val):
        m_tmp = clone(modelo)
        m_tmp.fit(X_val[tr], y_arr[tr])
        y_pred[te] = m_tmp.predict(X_val[te])
    res    = y_arr - y_pred
    mae    = float(np.mean(np.abs(res)))
    rmse   = float(np.sqrt(np.mean(res ** 2)))
    ss_res = float(np.sum(res ** 2))
    ss_tot = float(np.sum((y_arr - y_arr.mean()) ** 2))
    r2     = float(1 - ss_res / ss_tot) if ss_tot > 1e-10 else float("nan")
    abs_res = np.abs(res)
    mediana = float(np.median(abs_res))
    dp      = float(np.std(abs_res, ddof=1))
    return round(mae, 3), round(rmse, 3), round(r2, 3), round(mediana, 3), round(dp, 3)

def treinar_modelos(X, y, alvo):
    """
    Avalia modelos com LOO-CV (análise retrospectiva, features de execução).
    Retorna apenas as métricas de comparação — usadas no gráfico da aba
    "Comparação de Modelos". NÃO gera coeficientes para predição client-side
    (isso é feito por treinar_modelo_duracao_pre, com features pré-projeto).
    """
    print(f"\n🤖 Predição: {alvo}")
    idx = y.dropna().index
    Xv, yv = X.loc[idx], y.loc[idx]

    if len(yv) < 4:
        print("  ⚠️  Amostras insuficientes (< 4). Pulando predição.")
        return {}

    modelos = {
        "Ridge":             Pipeline([("sc", StandardScaler()), ("m", Ridge(alpha=1.0))]),
        "Lasso":             Pipeline([("sc", StandardScaler()), ("m", Lasso(alpha=0.5, max_iter=5000))]),
        "ElasticNet":        Pipeline([("sc", StandardScaler()), ("m", ElasticNet(alpha=0.5, max_iter=5000))]),
        "SVR":               Pipeline([("sc", StandardScaler()), ("m", SVR(kernel="rbf", C=10))]),
        "KNN":               Pipeline([("sc", StandardScaler()), ("m", KNeighborsRegressor(n_neighbors=min(3, len(yv)-1)))]),
        "Random Forest":     RandomForestRegressor(n_estimators=100, random_state=42, max_depth=3),
        "Grad. Boosting":    GradientBoostingRegressor(n_estimators=50, random_state=42, max_depth=2),
    }

    resultados = {}
    for nome, m in modelos.items():
        mae, rmse, r2, mediana, dp = _loo_metricas(m, Xv, yv)
        resultados[nome] = {"MAE": mae, "RMSE": rmse, "R2": r2, "Mediana": mediana, "DP": dp}
        print(f"   {nome:20s}  MAE={mae:.2f}  RMSE={rmse:.2f}  Mediana={mediana:.2f}  DP={dp:.2f}  R²={r2:+.3f}")

    melhor_nome = min(resultados, key=lambda k: resultados[k]["MAE"])
    print(f"   ⭐ Melhor (MAE): {melhor_nome} "
          f"(MAE={resultados[melhor_nome]['MAE']:.2f}  "
          f"R²={resultados[melhor_nome]['R2']:+.3f})")

    return resultados

def treinar_modelo_planejamento(X_plan_loo, X_plan_pred, Y_plan,
                                feat_plan_loo, feat_plan_pred, alvos_plan):
    X_plan  = X_plan_loo    # alias para o LOO-CV
    feat_plan = feat_plan_loo
    """
    Modelo 2: estimativa de planejamento multi-output com LOO-CV.
    Estrategia: avalia cada alvo univariado com LOO-CV, elege o vencedor
    por MAE medio, treina MultiOutputRegressor final em todos os dados e
    SERIALIZA O MODELO VENCEDOR (nao mais Ridge fixo) por alvo para a
    predicao no browser (JS). Se o vencedor nao for portavel para JS,
    faz fallback para o melhor modelo linear e registra aviso.
    """
    from sklearn.multioutput import MultiOutputRegressor

    print("\n🗂️  Modelo de Planejamento (multi-output):")
    print(f"   Features : {feat_plan}")
    print(f"   Alvos    : {alvos_plan}")

    idx = Y_plan.dropna(how="all").index
    Xv  = X_plan.loc[idx]
    Yv  = Y_plan.loc[idx]

    if len(Yv) < 4:
        print("  Amostras insuficientes. Pulando.")
        return {}, None, None

    scaler_plan = StandardScaler()
    Xv_sc = scaler_plan.fit_transform(Xv)

    loo = LeaveOneOut()

    # Candidatos: avaliados por media de MAE sobre todos os alvos (LOO-CV univariado)
    candidatos = {
        "Ridge":       Ridge(alpha=1.0),
        "Lasso":       Lasso(alpha=0.5, max_iter=5000),
        "ElasticNet":  ElasticNet(alpha=0.5, max_iter=5000),
        "KNN (k=3)":   KNeighborsRegressor(n_neighbors=min(3, len(Yv)-1)),
    }

    def _metricas_por_alvo(modelo, Xv, Yv, alvos_plan):
        """MAE, RMSE e R² médios via LOO acumulado por alvo."""
        maes, rmses, r2s, medianas, dps = [], [], [], [], []
        for alvo in alvos_plan:
            mae, rmse, r2, mediana, dp = _loo_metricas(modelo, Xv, Yv[alvo])
            maes.append(mae); rmses.append(rmse); r2s.append(r2)
            medianas.append(mediana); dps.append(dp)
        return (round(float(np.mean(maes)),    3),
                round(float(np.mean(rmses)),   3),
                round(float(np.nanmean(r2s)),  3),
                round(float(np.mean(medianas)),3),
                round(float(np.mean(dps)),     3))

    resultados_plan = {}
    for nome, est in candidatos.items():
        pipe = Pipeline([("sc", StandardScaler()), ("m", est)])
        mae, rmse, r2, mediana, dp = _metricas_por_alvo(pipe, Xv, Yv, alvos_plan)
        resultados_plan[nome] = {"MAE": mae, "RMSE": rmse, "R2": r2, "Mediana": mediana, "DP": dp}
        print(f"   {nome:14s}  MAE={mae:.2f}  RMSE={rmse:.2f}  Mediana={mediana:.2f}  DP={dp:.2f}  R²={r2:+.3f}")

    # Random Forest (multi-output nativo, avaliado por alvo)
    rf_pipe = Pipeline([("sc", StandardScaler()),
                        ("m", RandomForestRegressor(n_estimators=100,
                                                    random_state=42, max_depth=3))])
    mae_rf, rmse_rf, r2_rf, med_rf, dp_rf = _metricas_por_alvo(rf_pipe, Xv, Yv, alvos_plan)
    resultados_plan["Random Forest"] = {"MAE": mae_rf, "RMSE": rmse_rf, "R2": r2_rf, "Mediana": med_rf, "DP": dp_rf}
    print(f"   {'Random Forest':14s}  MAE={mae_rf:.2f}  RMSE={rmse_rf:.2f}  Mediana={mediana:.2f}  DP={dp:.2f}  R²={r2:+.3f}")

    melhor_nome = min(resultados_plan, key=lambda k: resultados_plan[k]["MAE"])
    print(f"   ⭐ Melhor (MAE): {melhor_nome}  "
          f"MAE={resultados_plan[melhor_nome]['MAE']:.2f}  "
          f"R²={resultados_plan[melhor_nome]['R2']:+.3f}")

    # Treinar MultiOutputRegressor final em todos os dados
    base = (RandomForestRegressor(n_estimators=100, random_state=42, max_depth=3)
            if melhor_nome == "Random Forest"
            else Pipeline([("sc", StandardScaler()),
                           ("m", candidatos.get(melhor_nome, Ridge(alpha=1.0)))]))
    melhor_multi = MultiOutputRegressor(base)
    melhor_multi.fit(Xv, Yv)

    # ── Serializar o MODELO VENCEDOR por alvo para predição client-side ──
    # O modelo serializado é o mesmo que venceu a avaliação LOO-CV (melhor_nome),
    # eliminando a contradição histórica (avaliava Lasso, serializava Ridge).
    # Se o vencedor não for portável para JS, cai no melhor LINEAR e registra aviso.
    idx_pred = Y_plan.dropna(how="all").index

    nome_serial = melhor_nome
    aviso_fallback = None
    if melhor_nome not in MODELOS_PORTAVEIS:
        lineares = {k: v for k, v in resultados_plan.items()
                    if k in ("Ridge", "Lasso", "ElasticNet")}
        nome_serial = min(lineares, key=lambda k: lineares[k]["MAE"])
        aviso_fallback = (
            f"Vencedor '{melhor_nome}' não é portável p/ JS; "
            f"serializando melhor linear '{nome_serial}'."
        )
        print(f"   ⚠️  {aviso_fallback}")

    coef_plan = {
        "alvos":        alvos_plan,
        "modelo":       nome_serial,          # rótulo real do que é servido
        "modelo_venc":  melhor_nome,          # vencedor absoluto (p/ auditoria)
        "features":     feat_plan_pred,       # features padrão (referência)
        "fallback":     aviso_fallback,
        "modelos":      {},                   # payload por alvo (via serializar_modelo)
    }

    for alvo in alvos_plan:
        # pe_eng_prev usa n_disc; demais usam feat_plan_pred padrão
        if alvo == "pe_eng_prev" and "n_disc" in X_plan_pred.columns:
            feat_alvo = [c for c in feat_plan_pred if c in X_plan_pred.columns]
        else:
            feat_alvo = [c for c in feat_plan_pred
                         if c in X_plan_pred.columns and c != "n_disc"]
        Xa = X_plan_pred.loc[idx_pred, feat_alvo].fillna(0)
        ya = Yv[alvo]
        payload = serializar_modelo(nome_serial, Xa, ya, feat_alvo)
        # auto-verificação de consistência interna (roda sem erro, finito)
        ok, _, _ = autoverificar_payload(payload, Xa, feat_alvo)
        if not ok:
            raise RuntimeError(
                f"Auto-verificação FALHOU para coef_plan[{alvo}] ({nome_serial})."
            )
        coef_plan["modelos"][alvo] = payload

    print(f"   💾 coef_plan serializado como '{nome_serial}' "
          f"(vencedor: {melhor_nome})")

    return resultados_plan, (melhor_nome, melhor_multi), coef_plan

