def treinar_modelo_duracao_pre(df: pd.DataFrame, encoders: dict):
    """
    Modelo de duração com features disponíveis ANTES de começar o projeto.
    Features: área + benfeitoria + tipo_om + tipo_obra + arq_reaprov.
    Alvos   : sprint_exec, duracao_dias.
    Serializa coeficientes Ridge para predição client-side no HTML.
    """
    from sklearn.multioutput import MultiOutputRegressor

    df = df.copy()
    for col in ("benfeitoria", "tipo_om", "tipo_obra"):
        enc = encoders.get(col)
        if enc:
            df[f"{col}_enc"] = enc.transform(df[col].fillna("desconhecido"))

    feat_pre = ["area", "benfeitoria_enc", "tipo_om_enc",
                "tipo_obra_enc", COL_ARQ_REAPROV]
    feat_pre = [c for c in feat_pre if c in df.columns]

    print(f"\n⏱️  Modelo Duração Pré-Projeto:")
    print(f"   Features: {feat_pre}")

    coefs_dur = {}
    for alvo in ["sprint_exec", "duracao_dias"]:
        y = df[alvo].dropna()
        Xv = df.loc[y.index, feat_pre].fillna(0)

        if len(y) < 4:
            print(f"   ⚠️  {alvo}: amostras insuficientes")
            continue

        # Avaliar com LOO-CV acumulado (MAE ≠ RMSE, R² válido)
        pipe = Pipeline([("sc", StandardScaler()), ("m", Ridge(alpha=1.0))])
        mae, rmse, r2, mediana, dp = _loo_metricas(pipe, Xv, y)
        print(f"   {alvo:15s}  MAE={mae:.2f}  RMSE={rmse:.2f}  R²={r2:+.3f}  "
              f"({mae / y.mean() * 100:.1f}% da média)")

        # Treinar Ridge final para serializar
        sc_ = StandardScaler().fit(Xv)
        r   = Ridge(alpha=1.0)
        r.fit(sc_.transform(Xv), y)
        coefs_dur[alvo] = {
            "intercept": float(r.intercept_),
            "coef":      r.coef_.tolist(),
            "mean":      sc_.mean_.tolist(),
            "scale":     sc_.scale_.tolist(),
            "features":  feat_pre,
        }

    return coefs_dur

