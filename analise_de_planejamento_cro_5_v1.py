# LOO-CV MAE relativo verificado nos 14 projetos (alvo *_prev, Lasso)
_CONFIANCA = {
    "EP":     {"mae_rel": 46, "cor": "yellow", "label": "Moderada"},
    "AP":     {"mae_rel": 92, "cor": "red",    "label": "Baixa"},
    "PE Arq": {"mae_rel": 27, "cor": "green",  "label": "Alta"},
    "PE Eng": {"mae_rel": 36, "cor": "yellow", "label": "Moderada"},
    "Orc":    {"mae_rel": 37, "cor": "yellow", "label": "Moderada"},
}
_ET_EXEC = [("EP","ep_exec"),("AP","ap_exec"),("PE Arq","pe_arq_exec"),
            ("PE Eng","pe_eng_exec"),("Orc","orc_exec")]
_ET_PREV = [("EP","ep_prev"),("AP","ap_prev"),("PE Arq","pe_arq_prev"),
            ("PE Eng","pe_eng_prev"),("Orc","orc_prev")]


class MotorAnalogia:
    def __init__(self):
        self.df = None; self._encs = {}
        self.area_min = self.area_max = 0.0; self._vecs = []

    def fit(self, df):
        self.df = df.copy().reset_index(drop=True)
        for col in ("tipo_obra","benfeitoria","tipo_om"):
            vals = sorted(self.df[col].dropna().unique().tolist())
            self._encs[col] = {v: i for i, v in enumerate(vals)}
        areas = self.df["area"].dropna()
        self.area_min, self.area_max = float(areas.min()), float(areas.max())
        self._vecs = [self._vec(r.to_dict()) for _, r in self.df.iterrows()]
        return self

    def _vec(self, row):
        v = []
        for col, peso in [("tipo_obra",3.0),("benfeitoria",2.0),("tipo_om",1.0)]:
            n = len(self._encs[col]); oh = [0.0]*n
            val = row.get(col)
            if val in self._encs[col]: oh[self._encs[col][val]] = 1.0
            v.extend(x*peso for x in oh)
        rng = self.area_max - self.area_min
        area_n = (float(row.get("area") or 0)-self.area_min)/rng if rng else 0.5
        v.append(area_n*2.0)
        nd = float(row.get("n_disc") or 4)
        v.append(max(0.0, min(1.0,(nd-2)/6))*2.0)
        v.append(float(row.get("arq_reaprov") or 0)*0.5)
        return np.array(v, dtype=float)

    @staticmethod
    def _cos(a, b):
        na, nb = np.linalg.norm(a), np.linalg.norm(b)
        return float(np.dot(a,b)/(na*nb)) if na>0 and nb>0 else 0.0

    def similares(self, query, k=3, excluir=None):
        qv = self._vec(query)
        sims = [(i,self._cos(qv,v)) for i,v in enumerate(self._vecs)
                if excluir is None or i not in excluir]
        sims.sort(key=lambda x:-x[1])
        return [(i,s,self.df.iloc[i]) for i,s in sims[:k]]

    def predizer(self, query, k=3, excluir=None):
        sims = self.similares(query, k=k, excluir=excluir)
        if not sims: return None, []
        tw = sum(s for _,s,_ in sims) or 1.0
        pred = {"pts_real":0.0, "sprint_exec_media":0.0}
        for _,col in _ET_EXEC: pred[col] = 0.0
        for (_,s,row) in sims:
            w = s/tw
            pred["pts_real"] += w*float(row.get("pts_real") or 0)
            pred["sprint_exec_media"] += w*float(row.get("sprint_exec") or 0)
            for _,col in _ET_EXEC: pred[col] += w*float(row.get(col) or 0)
        # AP condicional
        if _AP_REGRA.get(query.get("tipo_obra",""),"frequente") in ("nunca","raramente"):
            pred["ap_exec"] = 0.0
        return pred, sims

    def alertas_por_tipo(self):
        result = {}
        for tp in self._encs["tipo_obra"]:
            result[tp] = {}
            sub = self.df[self.df["tipo_obra"]==tp]
            for label,(pc,ec) in zip(["EP","AP","PE Arq","PE Eng","Orc"],
                                      [(p,e) for (_,p),(_,e) in zip(_ET_PREV,_ET_EXEC)]):
                s = sub[sub[pc]>0]
                if not len(s): continue
                v = ((s[ec]-s[pc])/s[pc]*100).values
                result[tp][label] = {
                    "media_var": round(float(np.mean(v)),1),
                    "pct_estouro": round(float(np.mean(v>0)*100),0),
                    "n": int(len(s)),
                }
        return result

    def loo_mae(self, k=3):
        erros = {"pts_real":[], "sprint_exec_media":[]}
        for _,col in _ET_EXEC: erros[col] = []
        for i,(_,row) in enumerate(self.df.iterrows()):
            pred,_ = self.predizer(row.to_dict(), k=k, excluir={i})
            if pred is None: continue
            erros["pts_real"].append(abs(pred["pts_real"]-float(row.get("pts_real") or 0)))
            erros["sprint_exec_media"].append(abs(pred["sprint_exec_media"]-float(row.get("sprint_exec") or 0)))
            for _,col in _ET_EXEC:
                erros[col].append(abs(pred[col]-float(row.get(col) or 0)))
        return {k:round(float(np.mean(v)),2) for k,v in erros.items() if v}

    def exportar_js(self):
        projetos = []
        for _,row in self.df.iterrows():
            p = {
                "id":int(row.get("id",0)), "om":str(row.get("om","")),
                "proj":str(row.get("projeto",""))[:40],
                "area":round(float(row.get("area") or 0),1),
                "tipo_obra":str(row.get("tipo_obra","")),
                "benfeitoria":str(row.get("benfeitoria","")),
                "tipo_om":str(row.get("tipo_om","")),
                "n_disc":int(row.get("n_disc") or 4),
                "arq_reaprov":int(row.get("arq_reaprov") or 0),
                "pts_real":float(row.get("pts_real") or 0),
                "sprint_exec":int(row.get("sprint_exec") or 0),
            }
            for _,col in _ET_EXEC: p[col] = float(row.get(col) or 0)
            projetos.append(p)
        return {
            "encs":     {k:v for k,v in self._encs.items()},
            "n_feats":  {k:len(v) for k,v in self._encs.items()},
            "area_min": self.area_min, "area_max": self.area_max,
            "vecs":     [v.tolist() for v in self._vecs],
            "projetos": projetos,
            "loo_mae":  self.loo_mae(),
            "alertas":  self.alertas_por_tipo(),
            "confianca":_CONFIANCA,
            "ap_regra": _AP_REGRA,
        }

def construir_dados_anuais(df: pd.DataFrame) -> dict:
    df2 = df.copy()
    df2["_ano"] = pd.to_datetime(df2["dt_inicio"], errors="coerce").dt.year
    anos = sorted(df2["_ano"].dropna().unique().astype(int))
    if not anos: return {}
    por_ano = {}
    for ano in anos:
        g = df2[df2["_ano"]==ano]
        var_et = {}
        for label,(pc,ec) in zip(["EP","AP","PE Arq","PE Eng","Orc"],
                                  [(p,e) for (_,p),(_,e) in zip(_ET_PREV,_ET_EXEC)]):
            s = g[g[pc]>0]
            if len(s): var_et[label] = round(float(((s[ec]-s[pc])/s[pc]*100).mean()),1)
        vp = ((g["pts_real"]-g["pts_prev"])/g["pts_prev"]*100)
        vs = ((g["sprint_exec"]-g["sprint_prev"])/g["sprint_prev"]*100)
        por_ano[str(ano)] = {
            "n": int(len(g)),
            "var_pts":    round(float(vp.mean()),1),
            "var_sprint": round(float(vs.mean()),1),
            "efic":       round(float((g["pts_real"]/g["pts_prev"].replace(0,np.nan)).mean()),2),
            "estouro_pts": round(float((vp>0).mean()*100),0),
            "estouro_spr": round(float((vs>0).mean()*100),0),
            "duracao_med": round(float(g["duracao_dias"].mean()),0),
            "var_etapa":  var_et,
        }
    tend = {}
    if len(anos)>=2:
        a0,a1 = str(anos[0]),str(anos[-1])
        for k in ["var_pts","var_sprint","efic","estouro_pts"]:
            tend[k] = round(por_ano[a1][k]-por_ano[a0][k],2)
    return {"anos":[str(a) for a in anos], "por_ano":por_ano, "tendencia":tend}

