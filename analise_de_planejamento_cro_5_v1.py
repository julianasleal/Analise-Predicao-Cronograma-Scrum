def _tree_to_dict(t):
    """Serializa uma árvore de decisão do sklearn em arrays enxutos."""
    T = t.tree_
    return {
        "cl": T.children_left.tolist(),
        "cr": T.children_right.tolist(),
        "f":  T.feature.tolist(),
        "th": [round(float(x), 6) for x in T.threshold.tolist()],
        "v":  [round(float(v[0][0]), 6) for v in T.value.tolist()],
    }

def serializar_modelo(nome, X, y, features):
    """
    Treina o modelo `nome` em (X, y) e retorna um payload com 'tipo' +
    parâmetros que o JS interpreta. Alvo único (chame por alvo).

      linear : intercept, coef, mean, scale
      knn    : X normalizado, y, k, mean, scale
      rf     : lista de árvores + n_features

    Levanta ValueError se o modelo não for portável para JS.
    """
    Xv = X.values if hasattr(X, "values") else np.asarray(X, float)
    yv = np.asarray(y, float)

    if nome in ("Ridge", "Lasso", "ElasticNet"):
        sc = StandardScaler().fit(Xv)
        est = {"Ridge":      Ridge(alpha=1.0),
               "Lasso":      Lasso(alpha=0.5, max_iter=5000),
               "ElasticNet": ElasticNet(alpha=0.5, max_iter=5000)}[nome]
        est.fit(sc.transform(Xv), yv)
        return {
            "tipo": "linear", "modelo": nome,
            "intercept": float(est.intercept_), "coef": est.coef_.tolist(),
            "mean": sc.mean_.tolist(), "scale": sc.scale_.tolist(),
            "features": list(features),
        }

    if nome.startswith("KNN"):
        k = int(min(3, len(yv) - 1))
        sc = StandardScaler().fit(Xv)
        Xn = sc.transform(Xv)
        return {
            "tipo": "knn", "modelo": nome, "k": k,
            "mean": sc.mean_.tolist(), "scale": sc.scale_.tolist(),
            "X": [row.tolist() for row in Xn], "y": yv.tolist(),
            "features": list(features),
        }

    if nome == "Random Forest":
        rf = RandomForestRegressor(n_estimators=100, random_state=42, max_depth=3)
        rf.fit(Xv, yv)
        return {
            "tipo": "rf", "modelo": nome,
            "trees": [_tree_to_dict(t) for t in rf.estimators_],
            "n_features": int(Xv.shape[1]), "features": list(features),
        }

    raise ValueError(
        f"Modelo '{nome}' não é portável para predição client-side. "
        f"Portáveis: {sorted(MODELOS_PORTAVEIS)}."
    )

def predizer_payload(payload, x_dict):
    """
    Reproduz em Python o cálculo client-side (JS). DEVE espelhar o JS.
    x_dict: {feature: valor}. Retorna predição com piso em 0.
    """
    feats = payload["features"]
    x = np.array([float(x_dict.get(f, 0)) for f in feats], float)

    if payload["tipo"] == "linear":
        mean = np.array(payload["mean"]); scale = np.array(payload["scale"])
        xn = (x - mean) / np.where(scale == 0, 1, scale)
        return max(0.0, payload["intercept"] + float(np.dot(payload["coef"], xn)))

    if payload["tipo"] == "knn":
        mean = np.array(payload["mean"]); scale = np.array(payload["scale"])
        xn = (x - mean) / np.where(scale == 0, 1, scale)
        Xtr = np.array(payload["X"]); ytr = np.array(payload["y"])
        d = np.sqrt(((Xtr - xn) ** 2).sum(axis=1))
        idx = np.argsort(d, kind="stable")[:payload["k"]]
        return max(0.0, float(ytr[idx].mean()))

    if payload["tipo"] == "rf":
        def walk(tree, xx):
            n = 0
            while tree["cl"][n] != tree["cr"][n]:
                n = tree["cl"][n] if xx[tree["f"][n]] <= tree["th"][n] else tree["cr"][n]
            return tree["v"][n]
        return max(0.0, float(np.mean([walk(t, x) for t in payload["trees"]])))

    raise ValueError(f"tipo desconhecido: {payload['tipo']}")

def autoverificar_payload(payload, X, features, tol=1e-6):
    """
    Compara predizer_payload (lógica JS) com o próprio payload aplicado às
    linhas de treino. Como não há um sklearn de referência aqui (o payload
    já É o modelo final), verificamos consistência interna: reexecuta a
    predição em cada linha e confirma que roda sem erro e é finita.
    Retorna (ok, max_abs, preds).
    """
    Xv = X.values if hasattr(X, "values") else np.asarray(X, float)
    preds = []
    for row in Xv:
        xd = {f: row[j] for j, f in enumerate(features)}
        preds.append(predizer_payload(payload, xd))
    arr = np.array(preds)
    ok = bool(np.all(np.isfinite(arr)))
    return ok, float(np.max(np.abs(arr))) if len(arr) else 0.0, preds

def verificar_contra_sklearn(payload, sklearn_fitted, X, features, tol=1e-6):
    """
    Verificação FORTE: compara a lógica JS (predizer_payload) com um modelo
    sklearn já treinado, projeto-a-projeto. Retorna (ok, max_diff).
    Use quando houver um estimador sklearn de referência disponível.
    """
    Xv = X.values if hasattr(X, "values") else np.asarray(X, float)
    sk = sklearn_fitted.predict(Xv)
    max_diff = 0.0
    for i, row in enumerate(Xv):
        xd = {f: row[j] for j, f in enumerate(features)}
        p_js = predizer_payload(payload, xd)
        p_sk = max(0.0, float(sk[i]))
        max_diff = max(max_diff, abs(p_js - p_sk))
    return (max_diff <= tol), max_diff

